/**
 * op_add - does specified math
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - does specified math
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - does specified math
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - does specified math
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod- does specified math
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
